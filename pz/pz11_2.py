#2.	Из предложенного текстового файла (text18-10.txt) вывести на экран его содержимое, количество букв в верхнем регистре. 
#Сформировать новый файл, в который поместить текст в стихотворной форме предварительно поставив после последней строки автора и название произведения.

# Чтение содержимого текстового файла
with open('text18-10.txt', 'r') as f:
    text = f.read()

# Подсчет количества букв в верхнем регистре
uppercase_count = sum(1 for char in text if char.isupper())

# Вывод содержимого файла и количества букв в верхнем регистре
print("Содержимое текстового файла:")
print(text)
print(f"Количество букв в верхнем регистре: {uppercase_count}")

# Создание стихотворной формы и формирование нового файла
poem = text.split('\n')
poem[len(poem)-1] = f"{poem[len(poem)-1]}\n\nАвтор: [Ваше имя]\nНазвание: [Название произведения]"

with open('poem.txt', 'w') as f:
    f.write('\n'.join(poem))
