# Список турагентств и предлагаемых ими туров
turagencies = {
    'Вояж': ['Мексика', 'Канада', 'Израиль', 'Италия', 'США'],
    'РейнаТур': ['Англия', 'Япония', 'Канада', 'ЮАР'],
    'Радуга': ['США', 'Испания', 'Швеция', 'Австралия'],
}

# 1. Турагентства, предлагающие туры в Японию
japan_agencies = [agency for agency, tours in turagencies.items() if 'Япония' in tours]
print("Турагентства, предлагающие туры в Японию:", japan_agencies)

# 2. Турагентства, не предлагающие туры в ЮАР
no_south_africa_agencies = [agency for agency, tours in turagencies.items() if 'ЮАР' not in tours]
print("Турагентства, не предлагающие туры в ЮАР:", no_south_africa_agencies)

# 3. Полный список всех туров
all_tours = []
for tours in turagencies.values():
    all_tours.extend(tours)
print("Полный список всех туров:", set(all_tours))  # используем множество для удаления дубликатов