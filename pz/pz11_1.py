#Сформировать новый текстовый файл (.txt) следующего вида, предварительно выполнив требуемую обработку элементов:
#Исходные данные:
#Количество элементов:
#Элементы в обратном порядке:
#Сумма элементов последней половины:

# Формирование текстового файла с последовательностью чисел
with open('sequence.txt', 'w') as f:
    numbers = [int(x) for x in input("Введите последовательность чисел через пробел: ").split()]
    f.write(' '.join(map(str, numbers)))

# Обработка чисел и формирование нового текстового файла
with open('sequence.txt', 'r') as f:
    numbers = [int(x) for x in f.read().split()]

with open('processed_sequence.txt', 'w') as f:
    f.write(f"Исходные данные: {numbers}\n")
    f.write(f"Количество элементов: {len(numbers)}\n")
    f.write(f"Элементы в обратном порядке: {numbers[::-1]}\n")
    f.write(f"Сумма элементов последней половины: {sum(numbers[len(numbers)//2:])}")